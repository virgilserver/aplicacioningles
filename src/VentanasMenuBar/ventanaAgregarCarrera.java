    private void formatField() {
        JTextFieldFilter jt = new JTextFieldFilter(JTextFieldFilter.ALPHADOT,45);
        fieldCarrera.setDocument(jt);
        JTextFieldFilter jt2 = new JTextFieldFilter(JTextFieldFilter.ALPHA,4);
        fieldId.setDocument(jt2);
    }

    private void llenarCampos() {
    try {
        ResultSet rs = ConexionSQLServer.datoCarreraId(idCarrera);
        
        while (rs.next()) {
            System.out.println(rs.getString(2).trim());
            this.fieldCarrera.setText(rs.getString(2).trim());
            this.fieldId.setText(rs.getString(3).trim());
            this.fieldId.setEnabled(false);
        }
    } catch (SQLException ex) {
        Logger.getLogger(ventanaAgregarCarrera.class.getName()).log(Level.SEVERE, null, ex);
    }
    
    }
