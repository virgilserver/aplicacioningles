    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String nombreCarrera = fieldCarrera.getText().toUpperCase();
        String claveCarrera = fieldId.getText().toUpperCase();
        
        if (nombreCarrera.isEmpty() || claveCarrera.isEmpty()) {
            
        }else{
            
            
            if (jButton1.getText().equals("MODIFICAR")) {
                if (ConexionSQLServer.modificarCarrera(idCarrera, nombreCarrera)) {
                   mensajesEnPantalla.mensajeExitoso("Carrera Modificada Correctment", "Registro");
                    dispose();
                    
                }
  
            } 
            else{
            if (ConexionSQLServer.registrarCarrera(nombreCarrera, claveCarrera)) {
                mensajesEnPantalla.mensajeExitoso("Carrera Registrada", "Registr0");
                dispose();
            }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

