    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String idioma = fieldIdioma.getText().toUpperCase();
        String clave = fieldClave.getText().toUpperCase();
        
        
        
        if (idioma.isEmpty() && clave.isEmpty()) {
            
        }else{
            
            if (this.jButton1.getText().equals("MODIFICAR")) {
                if (ConexionSQLServer.modificarIdioma(idIdioma, idioma)) {
                    mensajesEnPantalla.mensajeExitoso("Idioma Modificado correctamente", "Registro");
                    dispose();
                }
            }
            else{
             if (ConexionSQLServer.registrarIdioma(idioma, clave)) {
                 mensajesEnPantalla.mensajeExitoso("Idioma registrado","Registro");
                 dispose();
            }
            }
        }
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

