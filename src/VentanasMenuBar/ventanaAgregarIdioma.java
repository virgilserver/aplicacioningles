    private void formatField() {
       JTextFieldFilter tf1 = new JTextFieldFilter(JTextFieldFilter.ALPHA, 20);
        fieldIdioma.setDocument(tf1);
           JTextFieldFilter tf2 = new JTextFieldFilter(JTextFieldFilter.ALPHA, 3);
               fieldClave.setDocument(tf2);
        
    }

    private void llenarCampos() {
    try {
        ResultSet rs = ConexionSQLServer.datoIdiomaById(idIdioma);
        while (rs.next()) {
            this.fieldClave.setText(rs.getString(3).trim());
            this.fieldClave.setEnabled(false);
            this.fieldIdioma.setText(rs.getString(2).trim());
        }
    } catch (SQLException ex) {
        Logger.getLogger(ventanaAgregarIdioma.class.getName()).log(Level.SEVERE, null, ex);
    }
    
    }



