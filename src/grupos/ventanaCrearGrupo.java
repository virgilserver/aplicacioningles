    private void llenarCombos() {
         
        ididioma = new TreeMap<>();
        idmaestro = new TreeMap<>();
        idsalones = new TreeMap<>();
        idTipoCurso = new TreeMap<>();
        ResultSet rs = ConexionSQLServer.vistaIdiomas();
       String desc;
       int id;
        
        try {
             while (rs.next()) {
                desc = rs.getString(2).trim();
                id = rs.getInt(1);
                ididioma.put(desc, id);
                comboIdioma.addItem(desc);
             }} catch (SQLException ex) {
             Logger.getLogger(ventanaCrearGrupo.class.getName()).log(Level.SEVERE, null, ex);
         }
        
        
        
        
        rs = ConexionSQLServer.vistaIdSalon();
         try {
             while (rs.next()) {
                 desc = rs.getString(1).trim();
                 id = rs.getInt(2);
                 idsalones.put(desc, id);
                 comboSalon.addItem(desc);
             }} catch (SQLException ex) {
             Logger.getLogger(ventanaCrearGrupo.class.getName()).log(Level.SEVERE, null, ex);
         }
         
         
         rs = ConexionSQLServer.vistaNombreMaIdioma();
         try {
             while (rs.next()) {
                 desc = rs.getString(2).trim();
                 id = rs.getInt(1);
                 idmaestro.put(desc, id);
                 comboMaestro.addItem(desc);
             }} catch (SQLException ex) {
             Logger.getLogger(ventanaCrearGrupo.class.getName()).log(Level.SEVERE, null, ex);
         }
        
         
         rs = ConexionSQLServer.vistaTipoCurso();
         try {
             while (rs.next()) {
                 desc = rs.getString(2).trim();
                 id = rs.getInt(1);
                 idTipoCurso.put(desc, id);
                 comboCurso.addItem(desc);
                         
             }} catch (SQLException ex) {
             Logger.getLogger(ventanaCrearGrupo.class.getName()).log(Level.SEVERE, null, ex);
         }
        
    
    }
