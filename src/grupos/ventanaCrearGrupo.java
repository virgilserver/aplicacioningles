    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        TreeMap<String,Object> parametros = new TreeMap<>();
        String salon = comboSalon.getSelectedItem().toString().trim();
        int idsalon = idsalones.get(salon);
        String tipocurso = comboCurso.getSelectedItem().toString().trim();
        int idtipocurso = idTipoCurso.get(tipocurso);
        String maestro = comboMaestro.getSelectedItem().toString().trim();
        int idmaestr = idmaestro.get(maestro);
        String idioma = comboIdioma.getSelectedItem().toString().trim();
        int ididiom = ididioma.get(idioma);
        int nivel = Integer.parseInt( comboNivel.getSelectedItem().toString());
        

        String idCicloEscolar = ventanaPrincipal.idCicloEscolar;
        
        String hrInicio = spinnerHoraInicio.getModel().getValue().toString();
        String mnInicio = spinnerMinutosInicio.getModel().getValue().toString();
        String horaInicio = hrInicio + ":" + mnInicio + ":" + "00";
        Time timeInicio = Time.valueOf(horaInicio);
        
        String hrFinal =  spinnerHoraTerminacion.getModel().getValue().toString();
        String mnFinal = spinnerMinutosTerminacion.getModel().getValue().toString();
        String horaFinal = hrFinal + ":" + mnFinal + ":" + "00";
        Time timeFinal = Time.valueOf(horaFinal);
        
        if (hrInicio.length() == 1) {
            hrInicio = 0 + hrInicio;
        }
        if (hrFinal.length() == 1) {
            hrFinal = 0+ hrFinal;
        }
        String idGrupo = salon + idioma.substring(0, 3) + tipocurso.substring(0,3) +  nivel + idCicloEscolar  + hrInicio + mnInicio.charAt(0) + hrFinal + mnFinal.charAt(0);
        System.out.println(idGrupo);
        System.out.println(idGrupo.length());
        String claveGrupo = salon + hrInicio + mnInicio.charAt(0) + idCicloEscolar+tipocurso.substring(0, 1);
        
        parametros.put("1", idGrupo);
        parametros.put("2", idtipocurso);
        parametros.put("3", ididiom);
        parametros.put("4", idCicloEscolar);
        parametros.put("5", timeInicio);
        parametros.put("6", timeFinal);
        parametros.put("7", claveGrupo);
        parametros.put("8", idmaestr);
        parametros.put("9", nivel);
        parametros.put("10", idsalon);
        
      
        
        if ( ConexionSQLServer.insertarGrupo(parametros)) {
            dispose();
            mensajesEnPantalla.mensajeExitoso("Grupo registrado correctamente", "Registro");
            p.llenarTabla();
            p.setVisible(true);
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

